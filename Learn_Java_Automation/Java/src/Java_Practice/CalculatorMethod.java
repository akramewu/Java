package Java_Practice;

public class CalculatorMethod 
{
	public int sum(int a, int b)
	{
	  int c = a+b;
	  return c;
	}
	
	public int sub(int a, int b)
	{
	  int c = a-b;
	  return c;
	}
	
	public int multi(int a, int b)
	{
	  int c = a*b;
	  return c;
	}
	
	public int div(int a, int b)
	{
	  int c = a/b;
	  return c;
	}

}
